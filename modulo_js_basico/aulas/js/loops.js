var x = 0;

document.writeln("Iniciando o <code>while</code>...<br>");

while (x<10) {
    document.writeln("Número: "+x+"<br>");
    x++;
}

document.writeln("Finalizando o <code>while</code>...<br><br>");


document.writeln("Iniciando o <code>for</code>...<br>");
for (n=11; n<20; n++) {
    document.writeln("Número: "+n+"<br>");
}
document.writeln("Finalizando o <code>for</code>...<br><br>");